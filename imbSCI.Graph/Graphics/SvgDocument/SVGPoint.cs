using System;

namespace imbSCI.Graph.Graphics.SvgDocument
{
    public class SVGPoint
    {
        #region Constructors

        public SVGPoint(double _x, double _y)
        {
            this.x = _x;
            this.y = _y;
        }

        public SVGPoint() { }

        #endregion

        #region Attributes

        private double x;
        private double y;

        #endregion

        #region Properties

        public double X
        {
            get { return x; }
            set { x = value; }
        }

        public double Y
        {
            get { return y; }
            set { y = value; }
        }

        #endregion
    }
}
